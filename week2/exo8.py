 #7. Miles-per-Gallon
# # A car’s miles-per-gallon (MPG) can be calculated with the following formula:
#MPG = Miles driven / Gallons of gas used
#Write a program that asks the user for the number of miles driven and the gallons of gas used. It should calculate the car’s MPG and display the result.

Traveledmiles = float(input('How many miles did you travel? '))
Gas = float(input('How much gallon Gas did you spend? '))

MPG = Traveledmiles / Gas
print('Your total MPG is ', MPG)

